function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function formatTemplate(text, vars = {}) {
  return String(text).replace(/\{(\w+)\}/g, (match, key) => (
    Object.prototype.hasOwnProperty.call(vars, key) ? String(vars[key]) : match
  ));
}

function replaceKnownTerms(text, dictionary) {
  return Object.keys(dictionary)
    .sort((a, b) => b.length - a.length)
    .reduce((current, key) => current.replace(new RegExp(escapeRegExp(key), "g"), dictionary[key]), text);
}

export function normalizeLanguage(language) {
  if (language === "en") return "en";
  if (language === "km") return "km";
  return "zh";
}

export function translateUiText(language, text, vars) {
  const normalizedLanguage = normalizeLanguage(language);
  const source = String(text ?? "");
  if (normalizedLanguage === "zh") return formatTemplate(source, vars);

  const dictionary = dictionaries[normalizedLanguage] || {};
  const templated = dictionary[source];
  if (templated) return formatTemplate(templated, vars);

  const failureMatch = source.match(/^(保存失败|删除失败|加载失败|恢复失败|生成失败|导入失败|复制失败|粘贴失败|排产保存失败)：(.+)$/);
  if (failureMatch) {
    return `${dictionary[failureMatch[1]] || replaceKnownTerms(failureMatch[1], dictionary)}: ${replaceKnownTerms(failureMatch[2], dictionary)}`;
  }

  const template = dictionary[source] || replaceKnownTerms(source, dictionary);
  return formatTemplate(template, vars);
}

export function createTranslator(language) {
  const normalizedLanguage = normalizeLanguage(language);
  return (text, vars) => translateUiText(normalizedLanguage, text, vars);
}

export function getGridLocaleText(language) {
  const lang = normalizeLanguage(language);
  if (lang === "en") return enGridLocaleText;
  if (lang === "km") return kmGridLocaleText;
  return zhGridLocaleText;
}

export function formatCurrencyForLanguage(value, language) {
  const lang = normalizeLanguage(language);
  const localeMap = { en: "en-US", km: "km-KH", zh: "zh-CN" };
  const number = Number(value || 0);
  return number.toLocaleString(localeMap[lang] || "zh-CN", {
    style: "currency",
    currency: lang === "km" ? "KHR" : "CNY",
    maximumFractionDigits: 2,
  });
}

